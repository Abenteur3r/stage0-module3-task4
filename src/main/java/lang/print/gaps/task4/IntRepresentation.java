package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c = 'c';
        char h = 'h';
        char a = 'a';
        char r = 'r';
        int cChar = c;
        int hChar = h;
        int aChar = a;
        int rChar = r;
        System.out.println(cChar);
        System.out.println(hChar);
        System.out.println(aChar);
        System.out.println(rChar);
    }
}
